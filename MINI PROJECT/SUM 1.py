def add(a, b):
    return a + b
def subtraction(a, b):
    return a - b
def multiply(a, b):
    return a * b
def division(a, b):
    return a / b
def modules(a, b):
    return a % b
def floordivision(a, b):
    return a // b
def exponent(a, b):
    return a ** b
print("Addition:", add(10, 5))
print("subtraction:", subtraction(10, 5))
print("multiply:", multiply(10, 5))
print("division:", division(10, 5))
print("modules:", modules(10, 5))
print("floordivision:", floordivision(10, 5))
print("exponent:", exponent(10, 5))
