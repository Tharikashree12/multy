class fan:
    def __init__(self, brand, price, colour):
       self.brand = brand
       self.price = price
       self.colour = colour
s1 = fan("usha", 2500, "black")
s2 = fan("crompt", 4500, "white" )
print(s1.brand)
print(s1.price)    
print(s1.colour)
print(s2.brand)
print(s2.price)
print(s2.colour)
